    private void jbCalcular1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbCalcular1ActionPerformed
        // TODO add your handling code here:
        if (jtfSNome.getText().isBlank() || jtfDDataNasc.getText().isBlank()) {
            JOptionPane.showMessageDialog(this,
                    "Todos os campos devem ser preenchidos!");
            jtfSNome.requestFocus();
        } else {
            long dias;
            String nome, dtNasc, msg;
            LocalDate dtDNasc, hoje;

            nome = jtfSNome.getText().toUpperCase();
            dtNasc = jtfDDataNasc.getText();

            DateTimeFormatter dtBr = DateTimeFormatter.ofPattern("dd/MM/yyyy");
            try {
                dtDNasc = LocalDate.parse(dtNasc, dtBr);
                hoje = LocalDate.now();
                dias = dtDNasc.until(hoje, ChronoUnit.DAYS);

                if (dias < 0) {
                    JOptionPane.showMessageDialog(this, "Você não é o Marty McFly, "
                            + "por favor escolha uma data do passado!");
                    jtfDDataNasc.requestFocus();
                } else {
                    msg = nome + " você já viveu " + dias + " dias até hoje!";
                    jlMSG.setText(msg);
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(jPanel1, "Houve um erro ao calcular: \n\n"
                        + e.getMessage() + ".\n\nFormato esperado: dd/mm/aaaa",
                        ".:Erro:.", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jbCalcular1ActionPerformed

